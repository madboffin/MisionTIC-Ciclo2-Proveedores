    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        String nombre = this.txtNombre.getText().trim().toLowerCase();

        ProductoClase producto = this.ctlProducto.buscar(nombre);
        if (producto == null) {
            JOptionPane.showMessageDialog(this, "No se encontro el producto", "Alerta", JOptionPane.ERROR_MESSAGE);
            return;
        } 
        this.txtPrecio.setText(String.valueOf(producto.getPrecio_actual()));
        this.txtExistencia.setText(producto.getExistencia()+"");
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        ProductoClase clsProducto = this.getValidProductoFromFrm();

        boolean response = this.ctlProducto.crear(clsProducto);
        if (response) {
            JOptionPane.showMessageDialog(this, "Se guard√≥ el registro exitosamente");
        } else {
            JOptionPane.showMessageDialog(this, "Error al guardar el registro del producto", "Alerta", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

