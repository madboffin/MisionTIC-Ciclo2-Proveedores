private void clearFrm() {
        // actualizando formulario
        this.txtNombre.setText("");
        this.txtPrecio.setText("");
        this.txtExistencia.setText("");
        this.cbProveedor.setSelectedIndex(0);
    }

    private ProductoClase getValidProductoFromFrm() {

        // Capturar datos del formulario
        String nombre = this.txtNombre.getText().toLowerCase();
        String precio = this.txtPrecio.getText();
        String existencia = this.txtExistencia.getText();
        String nit = this.cbProveedor.getSelectedItem().toString();

        // Validar datos
        if (nombre.trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Indique el nombre del producto", "Alerta", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        if (!precio.trim().matches("^\\d+$")) {
            JOptionPane.showMessageDialog(this, "Indique precio", "Alerta", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        if (!existencia.trim().matches("^\\d+$")) {
            JOptionPane.showMessageDialog(this, "Indique n√∫mero de existencias", "Alerta", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        if (!nit.trim().matches("^\\d+$")) {
            JOptionPane.showMessageDialog(this, "Indique el NIT del proveedor", "Alerta", JOptionPane.ERROR_MESSAGE);
            return null;
        }

        // Crear clase producto
        ProductoClase producto = new ProductoClase();

        try {
            producto.setNombre(nombre);
            producto.setPrecio_actual(Integer.parseInt(precio));
            producto.setExistencia(Integer.parseInt(existencia));
            producto.setNit_proveedor(Integer.parseInt(nit));
        } catch (NumberFormatException e) {
            return null;
        }

        return producto;
    }
